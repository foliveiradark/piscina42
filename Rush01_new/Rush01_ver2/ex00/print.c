#include <unistd.h>

void	ft_print(char	**board)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{	
			write (1, &board[i][j], 1);
			if (j < 3)
				write(1, " ", 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}		
}

#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

int main(void)
{
	char array[30] = "carfadad";
	ft_putstr(array);
	return (0);
}